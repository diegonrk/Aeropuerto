    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
       
        List<CompanyaAerea> lstComp = leerCompanyasCSV();
        comp = cbxLstComp.getSelectedItem().toString();

        for (CompanyaAerea c : lstComp) {
            if (c.getNombre().equals(comp)) {
                 modificacion = cbxCambios.getSelectedItem().toString();
                if (modificacion.equals("Nombre")) {
                    c.setNombre(txtCambios.getText());
                    lblModCompNombre.setText(txtCambios.getText());

                } else if (modificacion.equals("Prefijo")) {
                    c.setPrefijo(Integer.parseInt(txtCambios.getText()));
                    lblModCompPrefijo.setText(txtCambios.getText());

                } else if (modificacion.equals("Código")) {
                    c.setCodigo(txtCambios.getText());
                    lblModCompCodigo.setText(txtCambios.getText());

                } else if (modificacion.equals("Dirección sede central")) {
                    c.setDireccion(txtCambios.getText());
                    lblModCompSedeCentral.setText(txtCambios.getText());

                } else if (modificacion.equals("Municipio sede central")) {
                    c.setMunicipio(txtCambios.getText());
                    lblModCompMunicipio.setText(txtCambios.getText());

                } else if (modificacion.equals("Teléfono información al pasajero")) {
                    c.setNumInfoPasajero(txtCambios.getText());
                    lblModCompTlfPasajeros.setText(txtCambios.getText());

                } else if (modificacion.equals("Teléfono información aeropuertos")) {
                    c.setNumInfoAeropuerto(txtCambios.getText());
                    lblModCompTlfAeropuertos.setText(txtCambios.getText());

                }

            }
        }

        actualizarCSVComp(lstComp);
        txtCambios.setText("");
    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

