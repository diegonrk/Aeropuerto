    private void cbxLstCompActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxLstCompActionPerformed
        comp = cbxLstComp.getSelectedItem().toString();
        List<CompanyaAerea> lstComp = leerCompanyasCSV();
        for (CompanyaAerea c : lstComp) {

            int x = c.getPrefijo();
            String xS = Integer.toString(x);
            if (c.getNombre().equals(comp)) {
                lblModCompPrefijo.setText(xS);
                lblModCompCodigo.setText(c.getCodigo());
                lblModCompNombre.setText(c.getNombre());
                lblModCompSedeCentral.setText(c.getDireccion());
                lblModCompMunicipio.setText(c.getMunicipio());
                lblModCompTlfPasajeros.setText(c.getNumInfoPasajero());
                lblModCompTlfAeropuertos.setText(c.getNumInfoAeropuerto());
                pnlModCompInfo.setVisible(true);
                pnlModificar.setVisible(true);
            }
        }

    }//GEN-LAST:event_cbxLstCompActionPerformed

